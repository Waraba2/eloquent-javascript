// Summary
// JavaScript programs may inspect and interfere with the document that the browser is displaying 
// through a data structure called the DOM. This data structure represents the browser’s model of 
// the document, and a JavaScript program can modify it to change the visible document.
//
// The DOM is organized like a tree, where elements are arranged hierarchically according to the 
// structure of the document. The objects representing elements have properties such as parentNode 
// and childNodes, which can be used to navigate through this tree.
//
// The way a document is displayed can be influenced by styling, both by attaching styles to nodes
// directly and by defining rules that match certain nodes. There are many different style 
// properties, such as color or display. JavaScript code can manipulate an element’s style directly
// through its style property.
